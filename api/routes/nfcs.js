/*
These are some possible NFC API routes

- POST /nfc/scan: To scan NFC-enabled tickets and retrieve the data
- POST /nfc/register: To register a new NFC-enabled ticket
- POST /nfc/validate: To validate an NFC-enabled ticket
- GET /nfc/tickets: To retrieve all NFC-enabled tickets
- GET /nfc/tickets/:id: To retrieve a specific NFC-enabled ticket by ID
- PUT /nfc/tickets/:id: To update an existing NFC-enabled ticket by ID
- DELETE /nfc/tickets/:id: To delete an NFC-enabled ticket by ID

*/